from pykawa.partial_wave_sums import partial_wave_sum_viscosity, partial_wave_sum_angular
import numpy.testing as npt
import numpy as np
import pytest

class TestCrossSection(object):

    def test_viscosity_sum(self):

        log10v, log10_sigmav, phase_shift_data = self._make_test_case_viscosity()
        sigmav = partial_wave_sum_viscosity(10**log10v, phase_shift_data)
        sigmav_stored = 10 ** log10_sigmav
        npt.assert_allclose(sigmav / sigmav_stored, 1, 2)

    def test_angular_sum(self):

        log10v, phase_shift_data = self._make_test_case_angular()
        theta = np.linspace(0, np.pi, 10000)
        dsigma = partial_wave_sum_angular(
            10**log10v, theta, phase_shift_data
        )
        sigma_analytic = partial_wave_sum_viscosity(
            10**log10v, phase_shift_data
        )

        for i in range(len(log10v)):
            sigma_numerical = self._integrate_viscosity(dsigma[i], theta)
            npt.assert_almost_equal(
                np.log10(sigma_numerical),
                np.log10(sigma_analytic[i]),
                decimal=5,
                err_msg=(
                    f"velocity index {i} (log10_v={log10v[i]:.4f}): "
                    f"numerical={np.log10(sigma_numerical):.6f}, "
                    f"analytic={np.log10(sigma_analytic[i]):.6f}"
                ),
            )

    def _integrate_viscosity(self, dsigma_row, theta):
        """2π ∫ (dσ/dΩ) sin²θ sinθ dθ via trapezoidal rule."""
        integrand = dsigma_row * np.sin(theta) ** 2 * np.sin(theta)
        return 2 * np.pi * np.trapz(integrand, theta)

